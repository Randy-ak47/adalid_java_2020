public class _01variables {
    public static void main(String[] args) {
        
        short varShort=200;
        int varInt=1000;
        long varLong=5000;
        double varDouble = 100.30;
        float varFloat = 343.4f;
        char varChar = 'd';
        System.out.println("----------PRIMITIVOS-------");
        System.out.println(" ");
        System.out.println("Short = " + varShort);
        System.out.println("Short en bit = " + Short.SIZE);
        System.out.println("Short en bytes = " + Short.BYTES);
        System.out.println("Short mínimo = " + Short.MIN_VALUE);
        System.out.println("Short máximo = " + Short.MAX_VALUE);
        System.out.println(" ");
        System.out.println("Int = " + varInt);
        System.out.println("Int en bit = " + Integer.SIZE);
        System.out.println("Int en bytes = " + Integer.BYTES);
        System.out.println("Int mínimo = " + Integer.MIN_VALUE);
        System.out.println("Int máximo = " + Integer.MAX_VALUE);
        System.out.println(" ");
        System.out.println("Long = " + varLong);
        System.out.println("Long en bit = " + Long.SIZE);
        System.out.println("Long en bytes = " + Long.BYTES);
        System.out.println("Long mínimo = " + Long.MIN_VALUE);
        System.out.println("Long máximo = " + Long.MAX_VALUE);
        System.out.println(" ");
        System.out.println(" ------PRIMITIVOS FLOTANTES--------- ");
        System.out.println(" ");
        System.out.println("Double = " + varDouble);
        System.out.println("Double en bit = " + Double.SIZE);
        System.out.println("Double en bytes = " + Double.BYTES);
        System.out.println("Double mínimo = " + Double.MIN_VALUE);
        System.out.println("Double máximo = " + Double.MAX_VALUE);
        System.out.println(" ");
        System.out.println("Float = " + varFloat);
        System.out.println("Float en bit = " + Float.SIZE);
        System.out.println("Float en bytes = " + Float.BYTES);
        System.out.println("Float mínimo = " + Float.MIN_VALUE);
        System.out.println("Float máximo = " + Float.MAX_VALUE);
        System.out.println(" ");
        System.out.println(" ------TIPO CARACTER--------- ");
        System.out.println(" ");
        System.out.println("Char = " + varChar);
        System.out.println("Char en bit = " + Character.SIZE);
        System.out.println("Char en bytes = " + Character.BYTES);
        System.out.println("Char mínimo = " + Character.MIN_VALUE);
        System.out.println("Char máximo = " + Character.MAX_VALUE);
        System.out.println(" ");
    }
}
